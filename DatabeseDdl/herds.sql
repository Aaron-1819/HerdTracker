DROP TABLE Herds;
CREATE TABLE Herds (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    herdName VARCHAR2(255) NOT NULL,
    landOwner VARCHAR2(255) NOT NULL,
    paddockNumber VARCHAR2(255) NOT NULL,
    cattleClass VARCHAR2(255) NOT NULL,
    headSize NUMBER NOT NULL,
    herdStatus VARCHAR2(255),
    auValue NUMBER(10, 2) NOT NULL,
    comments VARCHAR2(1000),
    dateEntered DATE NOT NULL,
    exitDate DATE NOT NULL,
    differenceInDays NUMBER,
    imageUrl VARCHAR2(255),
    createdAt TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updatedAt TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);