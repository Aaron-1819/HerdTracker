DROP TABLE Paddocks;

CREATE TABLE Paddocks (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    paddockName VARCHAR2(255) NOT NULL,
    paddockNumber VARCHAR2(255) NOT NULL,
    sizeAcres NUMBER NOT NULL,
    landOwner VARCHAR2(255) NOT NULL,
    costType VARCHAR2(255) NOT NULL,
    unitCost NUMBER(10, 2) NOT NULL,
    cropType VARCHAR2(255),
    yearSeeded NUMBER, 
    yearRenovated NUMBER,
    imageUrl VARCHAR2(255),
    createdAt TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updatedAt TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);
